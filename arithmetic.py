# Integer arithmetic
x = 10
y = 20
int_addition = x + y
int_subtraction = x - y
int_multiplication = x * y
int_division = x // y
int_modulus = x % y
print("x = " + str(x) + ", y = " + str(y))
print("integer x + y = " + str(int_addition))
print("integer x - y = " + str(int_subtraction))
print("integer x * y = " + str(int_multiplication))
print("integer x / y = " + str(int_division))
print("integer x % y = " + str(int_modulus))
print()

# Decimal arithmetic
a = 5.2
b = 10.4
float_addition = a + b
float_subtraction = a - b
float_multiplication = a * b
float_division = a / b
float_modulus = a % b
float_exponentiation = a**b
print("a = " + str(a) + ", b = " + str(b))
print(f"double a + b = {float_addition}")
print(f"double a - b = {float_subtraction}")
print(f"double a * b = {float_multiplication}")
print(f"double a / b = {float_division}")
print(f"double a % b = {float_modulus}")
print(f"double a ** b = {float_exponentiation}")
print()

# Mixed arithmetic
print("mixed x + b = " + str(x + b))
